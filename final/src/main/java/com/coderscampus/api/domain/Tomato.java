package com.coderscampus.api.domain;

public class Tomato {
	 private String name;
	    private String type;
	    private String seasonAvailable;
	    
	    
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public String getType() {
			return type;
		}
		public void setType(String type) {
			this.type = type;
		}
		public String getSeasonAvailable() {
			return seasonAvailable;
		}
		public void setSeasonAvailable(String seasonAvailable) {
			this.seasonAvailable = seasonAvailable;
		}

}




